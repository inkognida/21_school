#include "ft_printf.h"

void	ft_putstr(char *s, int *r_b)
{
	int	i;

	if (!(s))
	{
		write(1, "(null)", 6);
		*r_b += 6;
		return ;
	}
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		*r_b = *r_b + 1;
	}
}
