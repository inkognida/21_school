#include "ft_printf.h"

void	ft_putendl(char *s)
{
	int	i;

	if (!(s))
		return ;
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
